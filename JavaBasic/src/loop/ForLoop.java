package loop;

public class ForLoop {

	public static void main(String[] args) {
		for(int i=0; i <= 10; i++) {//(initialization; control- how many time run loop ; increment/decrement from initial value of i
			System.out.printf("Loop %d\n",i);//print Loop 0 to 10 total 11 time
			
		}

	}//main class end 

}//end class ForLoop
